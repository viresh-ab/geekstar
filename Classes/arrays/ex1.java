// arrays
public class Ex1 {
	public static void main(String[] args) {
//		Integer elements
		int[] arr =new int[3];
		arr[0]=5;
		arr[1]=2;
		arr[2]=3;
		System.out.println(arr[0]+", "+arr[1]+", "+arr[2]);
//      Float type array elements
		float[] arr1 =new float[3];
		arr1[0]=5.1f;
		arr1[1]=2.1f;
		arr1[2]=3.1f;
		System.out.println(arr1[0]+", "+arr1[1]+", "+arr1[2]);
		
	}

}
